namespace _02.Float_or_double
{
    using System;

    class floatOrDouble
    {
        static void Main(string[] args)
        {
            double a = 34.567839023;
            Console.WriteLine(a);
            float b = 12.345f;
            Console.WriteLine(b);
            double c = 8923.1234857;
            Console.WriteLine(c);
            float d = 3456.091f;
            Console.WriteLine(d);
        }
    }
}
