namespace _01.Declare_Variables
{
    using System;

    class declareVariables
    {
        static void Main(string[] args)
        {
            ushort a = 52130;
            Console.WriteLine(a);
            sbyte b = -115;
            Console.WriteLine(b);
            int c = 4825932;
            Console.WriteLine(c);
            byte d = 97;
            Console.WriteLine(d);
            short e = -1000;
            Console.WriteLine(e);
        }
    }
}
